CREATE TABLE PROFESSOR(
    PSSN numeric(9) NOT NULL,
    FNAME varchar(20),
    LNAME varchar(20),
    STREET_ADDRESS varchar(30),
    CITY varchar(20),
    STATE char(2),
    ZIP_CODE numeric(5),
    AREA_CODE numeric(3),
    DIGITS numeric(7),
    SEX char(1),
    TITLE varchar(20),
    SALARY varchar(10),
    PRIMARY KEY (PSSN)
);

CREATE TABLE DEPARTMENT(
    DNUM char(4) NOT NULL,
    DNAME varchar(30),
    DPHONE numeric(10),
    OFFICE_LOCATION varchar(20),
    CHAIR_SSN numeric(9) NOT NULL,
    PRIMARY KEY (DNUM),
    FOREIGN KEY (CHAIR_SSN) REFERENCES PROFESSOR(PSSN)
);

CREATE TABLE COURSE(
    CNUM numeric(5) NOT NULL,
    TITLE varchar(30),
    TEXTBOOK varchar(50),
    UNITS tinyint,
    CDNUM char(4) NOT NULL,
    PRIMARY KEY (CNUM),
    FOREIGN KEY (CNUM) REFERENCES DEPARTMENT(DNUM)
);

CREATE TABLE STUDENT(
    CWID numeric(9) NOT NULL,
    ADDRESS text,
    FNAME varchar(20),
    LNAME varchar(20),
    SPHONE numeric(10),
    SDNUM char(4) NOT NULL,
    PRIMARY KEY (CWID),
    FOREIGN KEY (SDNUM) REFERENCES DEPARTMENT(DNUM)
);


CREATE TABLE COURSE_SECTION(
    CSNUM numeric(5) NOT NULL,
    SNUM numeric(2) NOT NULL,
    CLASSROOM varchar(5),
    MEETING_DAYS varchar(4),
    START_TIME varchar(8),
    END_TIME varchar(8),
    SEATNUM numeric(2),
    CS_PSSN numeric(9) NOT NULL,
    PRIMARY KEY (CSNUM, SNUM),
    FOREIGN KEY (CS_PSSN) REFERENCES PROFESSOR(PSSN)
);

CREATE TABLE MINOR(
    MDNUM char(4) NOT NULL,
    MCWID numeric(9) NOT NULL,
    PRIMARY KEY (MDNUM, MCWID),
    FOREIGN KEY (MDNUM) REFERENCES DEPARTMENT(DNUM),
    FOREIGN KEY (MCWID) REFERENCES STUDENT(CWID)
);

CREATE TABLE ENROLL(
    ECWID numeric(9) NOT NULL,
    ECSNUM numeric(5) NOT NULL,
    ESNUM  numeric(2) NOT NULL,
    GRADE char(2),
    PRIMARY KEY (ECWID, ECSNUM, ESNUM),
    FOREIGN KEY (ECWID) REFERENCES STUDENT(CWID),
    FOREIGN KEY (ECSNUM) REFERENCES COURSE_SECTION(CSNUM),
    FOREIGN KEY (ECSNUM) REFERENCES COURSE_SECTION(SNUM)
);

CREATE TABLE PREREQUISITE(
    PCNUM numeric(5) NOT NULL,
    PRIMARY KEY (PCNUM),
    FOREIGN KEY (PCNUM) REFERENCES COURSE(CNUM)
);

CREATE TABLE PROF_DEGREE(
    DPSSN numeric(9) NOT NULL,
    DEGREE varchar(10),
    PRIMARY KEY (DPSSN),
    FOREIGN KEY (DPSSN) REFERENCES PROFESSOR(PSSN)
);

